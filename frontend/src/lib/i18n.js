function deepMerge(base, override = {}) {
  const result = { ...base }

  for (const [key, value] of Object.entries(override)) {
    if (value && typeof value === 'object' && !Array.isArray(value)) {
      result[key] = deepMerge(base?.[key] ?? {}, value)
    } else {
      result[key] = value
    }
  }

  return result
}

export function normalizeLocale(locale) {
  const normalized = String(locale ?? '')
    .trim()
    .toLowerCase()
    .split('-')[0]

  return SUPPORTED_LOCALES.includes(normalized) ? normalized : ''
}

export function getBrowserLocale() {
  if (typeof globalThis.navigator === 'undefined') {
    return 'fr'
  }

  const browserLocales = [
    ...(globalThis.navigator.languages ?? []),
    globalThis.navigator.language,
  ]

  for (const browserLocale of browserLocales) {
    const normalizedLocale = normalizeLocale(browserLocale)

    if (normalizedLocale) {
      return normalizedLocale
    }
  }

  return 'fr'
}

export function getStoredLocale() {
  if (typeof globalThis.localStorage === 'undefined') {
    return ''
  }

  const rawLocale = globalThis.localStorage.getItem(LOCALE_STORAGE_KEY)

  return normalizeLocale(rawLocale)
}

export function setStoredLocale(locale) {
  if (typeof globalThis.localStorage === 'undefined') {
    return
  }

  const normalizedLocale = normalizeLocale(locale)

  if (normalizedLocale) {
    globalThis.localStorage.setItem(LOCALE_STORAGE_KEY, normalizedLocale)
  }
}

export function getCurrentLocale() {
  return getStoredLocale() || getBrowserLocale() || 'fr'
}

export function getDirection(locale) {
  return locale === 'ar' ? 'rtl' : 'ltr'
}

export function translate(locale, key, replacements = {}) {
  const dictionary = messages[locale] ?? messages.fr
  const segments = key.split('.')
  let value = dictionary

  for (const segment of segments) {
    value = value?.[segment]
  }

  if (typeof value !== 'string' && locale !== 'fr') {
    value = messages.fr

    for (const segment of segments) {
      value = value?.[segment]
    }
  }

  if (typeof value !== 'string') {
    if (import.meta.env.DEV) {
      console.warn(`[i18n] Missing translation key "${key}" for locale "${locale}"`)
    }

    return key
  }

  return Object.entries(replacements).reduce((result, [replacementKey, replacementValue]) => {
    return result.replaceAll(`{${replacementKey}}`, String(replacementValue))
  }, value)
}

export function getDateLocale(locale) {
  const dateLocales = {
    ar: 'ar-MA',
    en: 'en-US',
    es: 'es-ES',
    nl: 'nl-NL',
    pt: 'pt-PT',
    it: 'it-IT',
    ru: 'ru-RU',
    fr: 'fr-FR',
  }

  return dateLocales[normalizeLocale(locale)] ?? dateLocales.fr
}
